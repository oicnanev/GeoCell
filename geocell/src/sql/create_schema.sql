-- Department, some departments will have access to Operations, other only normal search / analysis
CREATE TABLE department(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(150) NOT NULL,
    have_operations BOOLEAN NOT NULL
);

-- User
-- normal users can do simple searches, analyst users can do more complex searches,
-- if user belongs to a department with Operations, he can view the operation or in case
-- of 'is_operation_admin', he can edit Department Operations.
-- Superuser is for webapp / DB admin, can view logs, create/delete users/departments, deactivate users
CREATE TABLE auth_user (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username           VARCHAR(150) UNIQUE      NOT NULL,
    password           VARCHAR(128)             NOT NULL,
    name               VARCHAR(150)             NOT NULL,
    email              VARCHAR(254)             NOT NULL,
    date_joined        TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
    last_login         TIMESTAMP WITH TIME ZONE,
    is_active          BOOLEAN                  NOT NULL DEFAULT true,
    is_superuser       BOOLEAN                  NOT NULL,
    is_analyst         BOOLEAN                  NOT NULL,
    is_operation_admin BOOLEAN                  NOT NULL,
    map_type           VARCHAR(9)               NOT NULL DEFAULT 'standard' CHECK (map_type IN ('standard', 'satellite', 'hybrid', 'terrain')),
    show_grid          BOOLEAN                  NOT NULL DEFAULT false,
    show_counties      BOOLEAN                  NOT NULL DEFAULT false
);
CREATE INDEX idx_auth_user_username ON auth_user(username varchar_pattern_ops);
CREATE INDEX idx_auth_user_id ON auth_user(id);

CREATE TABLE user_department(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
