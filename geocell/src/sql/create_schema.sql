-- Trigger para atualizar updated_at cell table
CREATE OR REPLACE FUNCTION update_cell_updated_at()
    RETURNS TRIGGER AS $$
BEGIN
    IF NEW.updated_at IS NOT DISTINCT FROM OLD.updated_at THEN
        NEW.updated_at = CURRENT_TIMESTAMP;
    END IF;
    RETURN NEW;
END;
