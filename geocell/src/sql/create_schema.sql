-- FUNCTIONS AND TRIGGERS =======================================================================================
-- Function to update last_seen
CREATE OR REPLACE FUNCTION update_user_activity()
    RETURNS TRIGGER AS $$
BEGIN
    UPDATE user_activity
    SET
        last_seen = CURRENT_TIMESTAMP,
        last_path = NEW.last_path,
        last_ip = NEW.last_ip
    WHERE user_id = NEW.user_id;

    -- If not found, insert
    IF NOT FOUND THEN
        INSERT INTO user_activity (user_id, last_seen, last_path, last_ip)
        VALUES (NEW.user_id, CURRENT_TIMESTAMP, NEW.last_path, NEW.last_ip);
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Function to update user timestamp
CREATE OR REPLACE FUNCTION update_user_location_timestamp()
    RETURNS TRIGGER AS $$
