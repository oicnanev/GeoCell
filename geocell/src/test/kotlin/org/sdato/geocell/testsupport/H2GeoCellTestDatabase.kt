	fun create(): Environment {
		val dbName = "geocell_${UUID.randomUUID().toString().replace("-", "")}"
		val dataSource = JdbcDataSource().apply {
			setURL("jdbc:h2:mem:$dbName;MODE=PostgreSQL;DATABASE_TO_LOWER=TRUE;DEFAULT_NULL_ORDERING=HIGH;DB_CLOSE_DELAY=-1")
			user = "sa"
			password = ""
		}
		val jdbcTemplate = JdbcTemplate(dataSource)
		registerSpatialFunctions(jdbcTemplate)
		createSchema(jdbcTemplate)
		seedData(jdbcTemplate)

		val repository = JdbcCellRepository(jdbcTemplate)
		val service = CellService(repository, CellUpsertRequestValidator(), CellPolygonGenerator())
		val controller = CellController(service)
		return Environment(dataSource, jdbcTemplate, repository, service, controller)
	}

