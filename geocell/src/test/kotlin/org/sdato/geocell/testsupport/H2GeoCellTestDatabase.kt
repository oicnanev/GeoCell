object H2GeoCellSpatialFunctions {
	private val geometryFactory = GeometryFactory(PrecisionModel(), 4326)
	private val wktReader = WKTReader(geometryFactory)
	private val wktWriter = WKTWriter()

	@JvmStatic
	fun stMakePoint(x: Double, y: Double): Geometry =
		geometryFactory.createPoint(Coordinate(x, y))

	@JvmStatic
	fun stSetSrid(geometry: Geometry, srid: Int): Geometry = geometry

	@JvmStatic
	fun stX(geometry: Geometry): Double = geometry.coordinate.x

	@JvmStatic
	fun stY(geometry: Geometry): Double = geometry.coordinate.y

	@JvmStatic
	fun stDWithin(left: Geometry, right: Geometry, distanceMeters: Double): Boolean =
		stDistance(left, right) <= distanceMeters

	@JvmStatic
	fun stDistance(left: Geometry, right: Geometry): Double {
		val a = left.coordinate
		val b = right.coordinate
		val earthRadius = 6_371_000.0
		val lat1 = Math.toRadians(a.y)
		val lat2 = Math.toRadians(b.y)
		val deltaLat = Math.toRadians(b.y - a.y)
		val deltaLon = Math.toRadians(b.x - a.x)
		val h = sin(deltaLat / 2).pow(2) + cos(lat1) * cos(lat2) * sin(deltaLon / 2).pow(2)
		val c = 2 * atan2(sqrt(h), sqrt(1 - h))
		return earthRadius * c
	}

	@JvmStatic
	fun stMakeEnvelope(minLon: Double, minLat: Double, maxLon: Double, maxLat: Double, srid: Int): Geometry =
		geometryFactory.createPolygon(
			arrayOf(
				Coordinate(minLon, minLat),
				Coordinate(maxLon, minLat),
				Coordinate(maxLon, maxLat),
				Coordinate(minLon, maxLat),
				Coordinate(minLon, minLat)
			)
		)

	@JvmStatic
	fun stWithin(left: Geometry, right: Geometry): Boolean = left.within(right)

	@JvmStatic
	fun stIntersects(left: Geometry, right: Geometry): Boolean = left.intersects(right)

	@JvmStatic
	fun stGeogFromText(wkt: String): Geometry = wktReader.read(normalizeWkt(wkt))

	@JvmStatic
	fun stAsGeoJson(geometry: Geometry?): String? = geometry?.toGeoJson()

	@JvmStatic
	fun stCollect(geometry: Geometry): Geometry = geometry

	@JvmStatic
	fun stConcaveHull(geometry: Geometry, ratio: Double): Geometry = geometry.convexHull()

	@JvmStatic
	fun stEquals(left: Geometry, right: Geometry): Boolean = left.equalsTopo(right)

	private fun Geometry.toGeoJson(): String = when (geometryType.uppercase()) {
		"POINT" -> """{"type":"Point","coordinates":[${coordinate.x},${coordinate.y}]}"""
		"POLYGON" -> {
			val rings = (0 until numGeometries).joinToString(",") { index ->
				val geom = getGeometryN(index)
				val coords = geom.coordinates.joinToString(",") { "[${it.x},${it.y}]" }
				"[$coords]"
			}
			"""{"type":"Polygon","coordinates":[$rings]}"""
		}
		"GEOMETRYCOLLECTION" -> {
			val items = (0 until numGeometries).joinToString(",") { index ->
				getGeometryN(index).toGeoJson()
			}
			"""{"type":"GeometryCollection","geometries":[$items]}"""
		}
		else -> """{"type":"${geometryType}","wkt":"${wktWriter.write(this)}"}"""
	}

	private fun normalizeWkt(wkt: String): String {
		val withoutSrid = wkt.trim().removePrefix("SRID=4326;")
		val withoutZ = withoutSrid.replace(Regex("""\bZ\b"""), "")
		return withoutZ.replace(
			Regex("""(-?\d+(?:\.\d+)?)\s+(-?\d+(?:\.\d+)?)\s+-?\d+(?:\.\d+)?"""),
			"$1 $2"
		)
	}
}
